package de.zabuza.brainbridge.service;

/**
 * Enumeration for file extensions.
 * 
 * @author Zabuza {@literal <zabuza.dev@gmail.com>}
 *
 */
public enum EFileExtension {
	/**
	 * Extension for CSS files.
	 */
	CSS,
	/**
	 * Extension for HTML files.
	 */
	HTML,
	/**
	 * Extension for JPG image files.
	 */
	JPG,
	/**
	 * Extension for javascript files.
	 */
	JS,
	/**
	 * Extension for PNG image files.
	 */
	PNG,
	/**
	 * Extension for regular text files.
	 */
	TEXT,
	/**
	 * Used for files with unknown file extensions.
	 */
	UNKNOWN
}
